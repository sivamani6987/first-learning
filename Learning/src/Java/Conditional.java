package Java;

public class Conditional {
	
	public static void main(String args[]) {
	
		int a=20;
	
		if(a<18) {
			System.out.println("he is eligible");
		}
		else if(a<=18){
			System.out.println("he is not eligible");
		}
		else  {
			System.out.println("go away");
		}
		
		System.out.println("exit");
	}
	

}
